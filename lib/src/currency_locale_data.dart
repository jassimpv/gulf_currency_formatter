/// Resolves the ISO 4217 currency code for [locale] (e.g. `en_US`, `en-US`,
/// or `ar_SA`), using the country/region subtag. Returns `null` if the
/// locale has no region subtag, or the region isn't in [countryToCurrencyCode].
String? currencyCodeForLocale(String locale) {
  final List<String> parts = locale.split(RegExp('[_-]'));
  if (parts.length < 2) return null;
  return countryToCurrencyCode[parts[1].toUpperCase()];
}

/// The device's current locale, as an `intl`-style string (e.g. `en_US`).
String systemLocale() {
  final localeTag = PlatformDispatcher.instance.locale.toString();
  return localeTag.replaceAll('-', '_');
}
